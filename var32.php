<?php
/*
Para criar uma constante, use a define() função.

Sintaxe
define(name, value, case-insensitive)

Parâmetros:

name : especifica o nome da constante

value : especifica o valor da constante

case-insensitive : especifica se o nome da constante não deve diferenciar maiúsculas de minúsculas. 
O padrão é falso
*/

define("GREETING", "Welcome to W3Schools.com!");

echo GREETING;
?>
